package com.ppwallet.model;

public class Disputes {
String disputeId;
String transactionId;
String reasonId;
String reasonDesc;
String raisedbyUserId;
String userComment;	
String status;
String raisedOn;
String userType;
String referenceNo;

public String getDisputeId() {
	return disputeId;
}
public void setDisputeId(String disputeId) {
	this.disputeId = disputeId;
}
public String getTransactionId() {
	return transactionId;
}
public void setTransactionId(String transactionId) {
	this.transactionId = transactionId;
}
public String getReasonId() {
	return reasonId;
}
public void setReasonId(String reasonId) {
	this.reasonId = reasonId;
}
public String getReasonDesc() {
	return reasonDesc;
}
public void setReasonDesc(String reasonDesc) {
	this.reasonDesc = reasonDesc;
}
public String getRaisedbyUserId() {
	return raisedbyUserId;
}
public void setRaisedbyUserId(String raisedbyUserId) {
	this.raisedbyUserId = raisedbyUserId;
}
public String getUserComment() {
	return userComment;
}
public void setUserComment(String userComment) {
	this.userComment = userComment;
}
public String getStatus() {
	return status;
}
public void setStatus(String status) {
	this.status = status;
}
public String getRaisedOn() {
	return raisedOn;
}
public void setRaisedOn(String raisedOn) {
	this.raisedOn = raisedOn;
}
public String getUserType() {
	return userType;
}
public void setUserType(String userType) {
	this.userType = userType;
}
public String getReferenceNo() {
	return referenceNo;
}
public void setReferenceNo(String referenceNo) {
	this.referenceNo = referenceNo;
}


}
