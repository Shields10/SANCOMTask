package com.ppwallet.model;

public class Wallet {
String userId;
String walletId;
String walletDesc;
String userType;
String currencyId;
String currentBalance;
String lastUpdated;
String status;
String relationshipNo;

public String getUserId() {
	return userId;
}
public void setUserId(String userId) {
	this.userId = userId;
}
public String getWalletId() {
	return walletId;
}
public void setWalletId(String walletId) {
	this.walletId = walletId;
}
public String getWalletDesc() {
	return walletDesc;
}
public void setWalletDesc(String walletDesc) {
	this.walletDesc = walletDesc;
}
public String getUserType() {
	return userType;
}
public void setUserType(String userType) {
	this.userType = userType;
}
public String getCurrencyId() {
	return currencyId;
}
public void setCurrencyId(String currencyId) {
	this.currencyId = currencyId;
}
public String getCurrentBalance() {
	return currentBalance;
}
public void setCurrentBalance(String currentBalance) {
	this.currentBalance = currentBalance;
}
public String getLastUpdated() {
	return lastUpdated;
}
public void setLastUpdated(String lastUpdated) {
	this.lastUpdated = lastUpdated;
}
public String getStatus() {
	return status;
}
public void setStatus(String status) {
	this.status = status;
}
public String getRelationshipNo() {
	return relationshipNo;
}
public void setRelationshipNo(String relationshipNo) {
	this.relationshipNo = relationshipNo;
}


}
