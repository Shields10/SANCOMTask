package com.ppwallet.model;

public class Token {
String tokenNo;
String cardNo;
String relationshipNo;
String dateOfExpiry;
String userName;
String userType;
String assetType;
String currencyId;
String walletId;
String walletdesc;

public String getWalletdesc() {
	return walletdesc;
}
public void setWalletdesc(String walletdesc) {
	this.walletdesc = walletdesc;
}
public String getTokenNo() {
	return tokenNo;
}
public void setTokenNo(String tokenNo) {
	this.tokenNo = tokenNo;
}
public String getCardNo() {
	return cardNo;
}
public void setCardNo(String cardNo) {
	this.cardNo = cardNo;
}
public String getRelationshipNo() {
	return relationshipNo;
}
public void setRelationshipNo(String relationshipNo) {
	this.relationshipNo = relationshipNo;
}
public String getDateOfExpiry() {
	return dateOfExpiry;
}
public void setDateOfExpiry(String dateOfExpiry) {
	this.dateOfExpiry = dateOfExpiry;
}
public String getUserName() {
	return userName;
}
public void setUserName(String userName) {
	this.userName = userName;
}
public String getUserType() {
	return userType;
}
public void setUserType(String userType) {
	this.userType = userType;
}
public String getAssetType() {
	return assetType;
}
public void setAssetType(String assetType) {
	this.assetType = assetType;
}
public String getCurrencyId() {
	return currencyId;
}
public void setCurrencyId(String currencyId) {
	this.currencyId = currencyId;
}
public String getWalletId() {
	return walletId;
}
public void setWalletId(String walletId) {
	this.walletId = walletId;
}


}
