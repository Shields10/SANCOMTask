package com.ppwallet.model;

public class CardProduct {
	String productId;
	String productName; 
	String brandName;	
	String productStartDate; 
	String productEndDate;
	String allocatedBIN;	
	String productType; 
	String billingCycle; 
	String suspenseAccountNo; 
	String productInterchangeFixed; 
	String productInterchangeVariable; 
	String productStatus;
	String createdOn;
	
	
	public String getProductId() {
		return productId;
	}
	public void setProductId(String productId) {
		this.productId = productId;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}
	public String getBrandName() {
		return brandName;
	}
	public void setBrandName(String brandName) {
		this.brandName = brandName;
	}
	public String getProductStartDate() {
		return productStartDate;
	}
	public void setProductStartDate(String productStartDate) {
		this.productStartDate = productStartDate;
	}
	public String getProductEndDate() {
		return productEndDate;
	}
	public void setProductEndDate(String productEndDate) {
		this.productEndDate = productEndDate;
	}
	public String getAllocatedBIN() {
		return allocatedBIN;
	}
	public void setAllocatedBIN(String allocatedBIN) {
		this.allocatedBIN = allocatedBIN;
	}
	public String getProductType() {
		return productType;
	}
	public void setProductType(String productType) {
		this.productType = productType;
	}
	public String getBillingCycle() {
		return billingCycle;
	}
	public void setBillingCycle(String billingCycle) {
		this.billingCycle = billingCycle;
	}
	public String getSuspenseAccountNo() {
		return suspenseAccountNo;
	}
	public void setSuspenseAccountNo(String suspenseAccountNo) {
		this.suspenseAccountNo = suspenseAccountNo;
	}
	public String getProductInterchangeFixed() {
		return productInterchangeFixed;
	}
	public void setProductInterchangeFixed(String productInterchangeFixed) {
		this.productInterchangeFixed = productInterchangeFixed;
	}
	public String getProductInterchangeVariable() {
		return productInterchangeVariable;
	}
	public void setProductInterchangeVariable(String productInterchangeVariable) {
		this.productInterchangeVariable = productInterchangeVariable;
	}
	public String getProductStatus() {
		return productStatus;
	}
	public void setProductStatus(String productStatus) {
		this.productStatus = productStatus;
	}
	public String getCreatedOn() {
		return createdOn;
	}
	public void setCreatedOn(String createdOn) {
		this.createdOn = createdOn;
	}

}
